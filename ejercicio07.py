# Pide al usuario cuántas horas trabajó en la semana y cuánto gana por hora. Calcula su salario semanal.

horas_trabajadas=int(input("por favor escribe cuanta horas has trabajado en la semana:"))
pago_por_hora=int(input("por favor escribe cuanto ganas por hora:"))

salario_semanal= horas_trabajadas * pago_por_hora

print(f"tu salario semanal es de {salario_semanal}")


